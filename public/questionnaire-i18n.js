  function flattenNested(obj, prefix, out) {
    if (obj == null) return;
    if (typeof obj === 'string') {
      if (prefix) out[prefix] = obj;
      return;
    }
    if (Array.isArray(obj)) {
      obj.forEach((item, i) => {
        const key = prefix + '.' + i;
        if (typeof item === 'string') out[key] = item;
        else if (item && typeof item === 'object') {
          if ('title' in item && 'sub' in item) {
            out[key + '.title'] = item.title;
            out[key + '.sub'] = item.sub;
          } else flattenNested(item, key, out);
        }
      });
      return;
    }
    if (typeof obj === 'object') {
      Object.entries(obj).forEach(([k, v]) => {
        const key = prefix ? prefix + '.' + k : k;
        if (v && typeof v === 'object' && !Array.isArray(v) && typeof v.label === 'string' && (v.sub == null || typeof v.sub === 'string') && !('title' in v)) {
          out[key + '.label'] = v.label;
          if (v.sub) out[key + '.sub'] = v.sub;
        } else if (typeof v === 'string') {
          out[key] = v;
        } else {
          flattenNested(v, key, out);
        }
      });
    }
  }

